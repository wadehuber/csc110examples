package videoexamples.dataexpressions;

public class IntegerDivision {

	public static void main(String[] args) {
		
		System.out.println("15 / 5 = " + (15/5));
		System.out.println("24 / 4 = " + (24/4));
		
		System.out.println("15 / 6 = " + (15/6));
		System.out.println("15 % 6 = " + (15%6));
		
		System.out.println("24 / 5 = " + (24/5));
		System.out.println("24 % 5 = " + (24%5));
		
		
		System.out.println("5 % 5 = " + (5%5));
		System.out.println("15 % 5 = " + (15%5));

		System.out.println("15 / 6.0 = " + (15/6.0));
		System.out.println("24 / 5.0 = " + (24/5.0));
	}

}
